package com.example.fronttttttttttttttttttt;



public class Vaccin {
   public String VT;
   public int nb;

    public Vaccin(String VT, int nb) {
        this.VT = VT;
        this.nb = nb;
    }

    public Vaccin() {
        this.nb=0;
    }
    public String getVT() {
        return VT;
    }

    public void setVT(String VT) {
        this.VT = VT;
    }

    public int getNb() {
        return nb;
    }

    public void setNb(int nb) {
        this.nb = nb;
    }






}
